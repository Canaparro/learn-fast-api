# math_operations.py

def add(x, y):
    """Returns the sum of x and y."""
    return x + y

def multiply(x, y):
    """Returns the multiplication between x and y."""
    return x * y
def subtract(x, y):
    """Returns the difference between x and y."""
    return x - y
def divide(x, y):
    """Returns the division between x and y."""
    return x / y
